package pittheo.projects.template.utils;

public class EnvProperties {

	private String domainName;
	
	private String imageServerUri;
	
	private String imageServerUsersUri;
	
	private String imageServerThemesUri;

	public String getDomainName() {
		return domainName;
	}

	public void setDomainName(String domainName) {
		this.domainName = domainName;
	}

	public String getImageServerUri() {
		return imageServerUri;
	}

	public void setImageServerUri(String imageServerUri) {
		this.imageServerUri = imageServerUri;
	}

	public String getImageServerUsersUri() {
		return imageServerUsersUri;
	}

	public void setImageServerUsersUri(String imageServerUsersUri) {
		this.imageServerUsersUri = imageServerUsersUri;
	}

	public String getImageServerThemesUri() {
		return imageServerThemesUri;
	}

	public void setImageServerThemesUri(String imageServerThemesUri) {
		this.imageServerThemesUri = imageServerThemesUri;
	}
	
}
